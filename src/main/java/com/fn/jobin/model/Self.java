package com.fn.jobin.model;


public class Self {
	private int selfCode;
	private String selfContent;
	private String selfSubject;
	private int resumeCode;
	
	public int getSelfCode() {
		return selfCode;
	}
	public void setSelfCode(int selfCode) {
		this.selfCode = selfCode;
	}
	public String getSelfContent() {
		return selfContent;
	}
	public void setSelfContent(String selfContent) {
		this.selfContent = selfContent;
	}
	public String getSelfSubject() {
		return selfSubject;
	}
	public void setSelfSubject(String selfSubject) {
		this.selfSubject = selfSubject;
	}
	public int getResumeCode() {
		return resumeCode;
	}
	public void setResumeCode(int resumeCode) {
		this.resumeCode = resumeCode;
	}
	
}
